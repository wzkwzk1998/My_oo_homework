package Elevator2;
import java.util.*;

public class RequireItem {
	protected int targetFloor;
	protected int gotTime;
	protected int eleState;     //1是向上-1向下
	
	public RequireItem() {
		
	}
	
	public int getTargetFloor() {
		return this.getTargetFloor();
	}
	public int getGotTime() {
		return this.getGotTime();
	}
	public int getEleState() {
		return this.eleState;
	}
}
